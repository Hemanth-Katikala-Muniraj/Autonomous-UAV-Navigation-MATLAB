function reportOutputs = generateMissionReportPlots( ...
    telemetryInput, summaryInput, config, occupancyGrid)
%GENERATEMISSIONREPORTPLOTS Generate portfolio-quality mission reports.
%
% Stage 11 reporting upgrade
%
% Generated figures:
%
%   01_mission_trajectory_top_view.png
%   02_mission_trajectory_3d.png
%   03_flight_speed_and_wind.png
%   04_bird_encounter_and_apf.png
%   05_final_occupancy_map.png
%   06_mapping_progress.png
%   07_refined_landing_performance.png
%   08_mission_performance_summary.png
%
% The function uses mission data recorded by the simulation. It does not
% create artificial example trajectories or fabricated metrics.
%
% TELEMETRYINPUT may be:
%   - A MATLAB table
%   - A path to missionTelemetry.csv
%
% SUMMARYINPUT may be:
%   - A MATLAB table
%   - A summary structure
%   - A path to missionSummary.csv
%
% Example:
%
%   generateMissionReportPlots( ...
%       "output/data/missionTelemetry.csv", ...
%       "output/data/missionSummary.csv", ...
%       config, ...
%       occupancyGrid);

    %% Resolve project root
    if isfield(config, "projectRoot") && ...
            strlength(string(config.projectRoot)) > 0

        projectRoot = string(config.projectRoot);

    else

        currentFile = mfilename("fullpath");

        projectRoot = string( ...
            fileparts( ...
                fileparts(currentFile)));
    end

    %% Create output directory
    plotsDirectory = fullfile( ...
        projectRoot, ...
        "output", ...
        "plots");

    if ~isfolder(plotsDirectory)
        mkdir(plotsDirectory);
    end

    %% Load telemetry
    telemetryTable = loadTelemetryTable( ...
        telemetryInput);

    summaryData = loadSummaryData( ...
        summaryInput);

    %% Validate telemetry
    if isempty(telemetryTable) || ...
            height(telemetryTable) == 0

        error( ...
            "MissionReport:EmptyTelemetry", ...
            "No telemetry data is available for report generation.");
    end

    %% Normalize telemetry variable access
    time = getNumericColumn( ...
        telemetryTable, ...
        "TimeSeconds", ...
        height(telemetryTable), ...
        0);

    positionX = getNumericColumn( ...
        telemetryTable, ...
        "PositionXMetres", ...
        height(telemetryTable), ...
        NaN);

    positionY = getNumericColumn( ...
        telemetryTable, ...
        "PositionYMetres", ...
        height(telemetryTable), ...
        NaN);

    positionZ = getNumericColumn( ...
        telemetryTable, ...
        "PositionZMetres", ...
        height(telemetryTable), ...
        NaN);

    groundSpeed = getNumericColumn( ...
        telemetryTable, ...
        "GroundSpeedMetresPerSecond", ...
        height(telemetryTable), ...
        NaN);

    airSpeed = getNumericColumn( ...
        telemetryTable, ...
        "AirSpeedMetresPerSecond", ...
        height(telemetryTable), ...
        NaN);

    windSpeed = getNumericColumn( ...
        telemetryTable, ...
        "WindSpeedMetresPerSecond", ...
        height(telemetryTable), ...
        NaN);

    windX = getNumericColumn( ...
        telemetryTable, ...
        "WindXMetresPerSecond", ...
        height(telemetryTable), ...
        0);

    windY = getNumericColumn( ...
        telemetryTable, ...
        "WindYMetresPerSecond", ...
        height(telemetryTable), ...
        0);

    windZ = getNumericColumn( ...
        telemetryTable, ...
        "WindZMetresPerSecond", ...
        height(telemetryTable), ...
        0);

    groundVelocityZ = getNumericColumn( ...
        telemetryTable, ...
        "GroundVelocityZMetresPerSecond", ...
        height(telemetryTable), ...
        NaN);

    yawDegrees = getNumericColumn( ...
        telemetryTable, ...
        "YawDegrees", ...
        height(telemetryTable), ...
        NaN);

    targetDistance = getNumericColumn( ...
        telemetryTable, ...
        "TargetDistanceMetres", ...
        height(telemetryTable), ...
        NaN);

    distanceTravelled = getNumericColumn( ...
        telemetryTable, ...
        "DistanceTravelledMetres", ...
        height(telemetryTable), ...
        NaN);

    birdPositionX = getNumericColumn( ...
        telemetryTable, ...
        "BirdPositionXMetres", ...
        height(telemetryTable), ...
        NaN);

    birdPositionY = getNumericColumn( ...
        telemetryTable, ...
        "BirdPositionYMetres", ...
        height(telemetryTable), ...
        NaN);

    birdPositionZ = getNumericColumn( ...
        telemetryTable, ...
        "BirdPositionZMetres", ...
        height(telemetryTable), ...
        NaN);

    birdDistance = getNumericColumn( ...
        telemetryTable, ...
        "BirdDistanceMetres", ...
        height(telemetryTable), ...
        NaN);

    birdClearance = getNumericColumn( ...
        telemetryTable, ...
        "BirdClearanceMetres", ...
        height(telemetryTable), ...
        NaN);

    birdLidarHits = getNumericColumn( ...
        telemetryTable, ...
        "BirdLidarHits", ...
        height(telemetryTable), ...
        0);

    apfActive = getLogicalColumn( ...
        telemetryTable, ...
        "APFAvoidanceActive", ...
        height(telemetryTable));

    emergencyActive = getLogicalColumn( ...
        telemetryTable, ...
        "APFEmergencyActive", ...
        height(telemetryTable));

    repulsiveSpeed = getNumericColumn( ...
        telemetryTable, ...
        "RepulsiveSpeedMetresPerSecond", ...
        height(telemetryTable), ...
        0);

    tangentialSpeed = getNumericColumn( ...
        telemetryTable, ...
        "TangentialSpeedMetresPerSecond", ...
        height(telemetryTable), ...
        0);

    mapCoverage = getNumericColumn( ...
        telemetryTable, ...
        "MapCoveragePercent", ...
        height(telemetryTable), ...
        NaN);

    mapFreeCells = getNumericColumn( ...
        telemetryTable, ...
        "MapFreeCells", ...
        height(telemetryTable), ...
        NaN);

    mapOccupiedCells = getNumericColumn( ...
        telemetryTable, ...
        "MapOccupiedCells", ...
        height(telemetryTable), ...
        NaN);

    mapUpdateCount = getNumericColumn( ...
        telemetryTable, ...
        "MapUpdateCount", ...
        height(telemetryTable), ...
        NaN);

    fpvBirdVisible = getLogicalColumn( ...
        telemetryTable, ...
        "FPVBirdVisible", ...
        height(telemetryTable));

    fpvForwardObstacle = getNumericColumn( ...
        telemetryTable, ...
        "FPVForwardObstacleDistanceMetres", ...
        height(telemetryTable), ...
        NaN);

    waypointIndex = getNumericColumn( ...
        telemetryTable, ...
        "WaypointIndex", ...
        height(telemetryTable), ...
        1);

    missionState = getStringColumn( ...
        telemetryTable, ...
        "MissionState", ...
        height(telemetryTable), ...
        "UNKNOWN");

    %% Derive useful signals
    horizontalError = hypot( ...
        positionX - config.mission.goalPosition(1), ...
        positionY - config.mission.goalPosition(2));

    wrappedYawError = wrapAngleDegrees( ...
        yawDegrees - ...
        rad2deg(config.landing.targetYaw));

    commandedSpeedEstimate = estimateCommandedSpeed( ...
        time, ...
        positionX, ...
        positionY, ...
        positionZ);

    birdActiveMask = ...
        isfinite(birdClearance);

    apfNumeric = ...
        double(apfActive);

    emergencyNumeric = ...
        double(emergencyActive);

    landingStartIndex = find( ...
        startsWith(missionState, "LANDING") | ...
        missionState == "TOUCHDOWN_VERIFY" | ...
        missionState == "COMPLETE", ...
        1, ...
        "first");

    if isempty(landingStartIndex)
        landingStartIndex = height(telemetryTable);
    end

    landingStartTime = ...
        time(landingStartIndex);

    touchdownIndex = find( ...
        missionState == "TOUCHDOWN_VERIFY", ...
        1, ...
        "first");

    if isempty(touchdownIndex)

        touchdownIndex = find( ...
            missionState == "COMPLETE", ...
            1, ...
            "first");
    end

    if isempty(touchdownIndex)

        touchdownTime = time(end);

    else

        touchdownTime = time(touchdownIndex);
    end

    %% Output structure
    reportOutputs = struct();

    reportOutputs.outputDirectory = ...
        plotsDirectory;

    reportOutputs.files = strings(0, 1);

    %% Shared appearance
    figureBackground = [0.055, 0.065, 0.080];
    axesBackground = [0.075, 0.085, 0.105];
    foregroundColor = [0.92, 0.94, 0.97];
    gridColor = [0.42, 0.47, 0.54];

    %% ---------------------------------------------------------------
    % 1. Mission trajectory — top view
    % ---------------------------------------------------------------
    trajectoryTopFigure = createReportFigure( ...
        "Mission Trajectory — Top View", ...
        figureBackground);

    trajectoryTopAxes = axes( ...
        "Parent", trajectoryTopFigure);

    hold(trajectoryTopAxes, "on");

    configureAxes( ...
        trajectoryTopAxes, ...
        axesBackground, ...
        foregroundColor, ...
        gridColor);

    drawRoadTopView( ...
        trajectoryTopAxes, ...
        config);

    drawOccupancyMapBackdrop( ...
        trajectoryTopAxes, ...
        occupancyGrid, ...
        config, ...
        0.22);

    plannedWaypoints = ...
        config.mission.waypoints;

    plot( ...
        trajectoryTopAxes, ...
        plannedWaypoints(:, 1), ...
        plannedWaypoints(:, 2), ...
        "--", ...
        "LineWidth", 1.4, ...
        "DisplayName", "Planned route");

    plot( ...
        trajectoryTopAxes, ...
        positionX, ...
        positionY, ...
        "-", ...
        "LineWidth", 2.4, ...
        "DisplayName", "Actual UAV trajectory");

    plotActiveTrajectorySegments( ...
        trajectoryTopAxes, ...
        positionX, ...
        positionY, ...
        apfActive, ...
        "APF active");

    validBirdPath = ...
        isfinite(birdPositionX) & ...
        isfinite(birdPositionY);

    if any(validBirdPath)

        plot( ...
            trajectoryTopAxes, ...
            birdPositionX(validBirdPath), ...
            birdPositionY(validBirdPath), ...
            "-.", ...
            "LineWidth", 1.8, ...
            "DisplayName", "Bird trajectory");
    end

    scatter( ...
        trajectoryTopAxes, ...
        plannedWaypoints(:, 1), ...
        plannedWaypoints(:, 2), ...
        55, ...
        "filled", ...
        "DisplayName", "Waypoints");

    scatter( ...
        trajectoryTopAxes, ...
        positionX(1), ...
        positionY(1), ...
        110, ...
        "^", ...
        "filled", ...
        "DisplayName", "Takeoff");

    scatter( ...
        trajectoryTopAxes, ...
        positionX(end), ...
        positionY(end), ...
        110, ...
        "v", ...
        "filled", ...
        "DisplayName", "Landing");

    closestBirdIndex = findFiniteMinimumIndex( ...
        birdClearance);

    if ~isempty(closestBirdIndex)

        scatter( ...
            trajectoryTopAxes, ...
            positionX(closestBirdIndex), ...
            positionY(closestBirdIndex), ...
            120, ...
            "p", ...
            "filled", ...
            "DisplayName", "Closest bird encounter");

        text( ...
            trajectoryTopAxes, ...
            positionX(closestBirdIndex) + 0.35, ...
            positionY(closestBirdIndex) + 0.35, ...
            sprintf( ...
                "Min clearance %.2f m", ...
                birdClearance(closestBirdIndex)), ...
            "Color", foregroundColor, ...
            "FontWeight", "bold", ...
            "FontSize", 9);
    end

    addDirectionArrows2D( ...
        trajectoryTopAxes, ...
        positionX, ...
        positionY);

    axis(trajectoryTopAxes, "equal");

    xlim( ...
        trajectoryTopAxes, ...
        config.world.xLimits);

    ylim( ...
        trajectoryTopAxes, ...
        config.world.yLimits);

    xlabel( ...
        trajectoryTopAxes, ...
        "World X position (m)");

    ylabel( ...
        trajectoryTopAxes, ...
        "World Y position (m)");

    title( ...
        trajectoryTopAxes, ...
        "Autonomous UAV Mission Trajectory — Top View");

    legend( ...
        trajectoryTopAxes, ...
        "Location", ...
        "bestoutside");

    topViewFile = fullfile( ...
        plotsDirectory, ...
        "01_mission_trajectory_top_view.png");

    exportReportFigure( ...
        trajectoryTopFigure, ...
        topViewFile);

    reportOutputs.files(end + 1, 1) = topViewFile;

    %% ---------------------------------------------------------------
    % 2. Mission trajectory — 3D
    % ---------------------------------------------------------------
    trajectory3DFigure = createReportFigure( ...
        "Mission Trajectory — 3D", ...
        figureBackground);

    trajectory3DAxes = axes( ...
        "Parent", trajectory3DFigure);

    hold(trajectory3DAxes, "on");

    configureAxes( ...
        trajectory3DAxes, ...
        axesBackground, ...
        foregroundColor, ...
        gridColor);

    drawRoad3D( ...
        trajectory3DAxes, ...
        config);

    plot3( ...
        trajectory3DAxes, ...
        plannedWaypoints(:, 1), ...
        plannedWaypoints(:, 2), ...
        plannedWaypoints(:, 3), ...
        "--", ...
        "LineWidth", 1.4, ...
        "DisplayName", "Planned route");

    plot3( ...
        trajectory3DAxes, ...
        positionX, ...
        positionY, ...
        positionZ, ...
        "-", ...
        "LineWidth", 2.4, ...
        "DisplayName", "Actual UAV trajectory");

    if any(validBirdPath)

        plot3( ...
            trajectory3DAxes, ...
            birdPositionX(validBirdPath), ...
            birdPositionY(validBirdPath), ...
            birdPositionZ(validBirdPath), ...
            "-.", ...
            "LineWidth", 1.8, ...
            "DisplayName", "Bird trajectory");
    end

    scatter3( ...
        trajectory3DAxes, ...
        plannedWaypoints(:, 1), ...
        plannedWaypoints(:, 2), ...
        plannedWaypoints(:, 3), ...
        55, ...
        "filled", ...
        "DisplayName", "Waypoints");

    scatter3( ...
        trajectory3DAxes, ...
        positionX(1), ...
        positionY(1), ...
        positionZ(1), ...
        110, ...
        "^", ...
        "filled", ...
        "DisplayName", "Takeoff");

    scatter3( ...
        trajectory3DAxes, ...
        positionX(end), ...
        positionY(end), ...
        positionZ(end), ...
        110, ...
        "v", ...
        "filled", ...
        "DisplayName", "Landing");

    if ~isempty(closestBirdIndex)

        plot3( ...
            trajectory3DAxes, ...
            [ ...
                positionX(closestBirdIndex), ...
                birdPositionX(closestBirdIndex)], ...
            [ ...
                positionY(closestBirdIndex), ...
                birdPositionY(closestBirdIndex)], ...
            [ ...
                positionZ(closestBirdIndex), ...
                birdPositionZ(closestBirdIndex)], ...
            ":", ...
            "LineWidth", 2.0, ...
            "DisplayName", "Minimum-clearance line");
    end

    xlabel( ...
        trajectory3DAxes, ...
        "World X position (m)");

    ylabel( ...
        trajectory3DAxes, ...
        "World Y position (m)");

    zlabel( ...
        trajectory3DAxes, ...
        "Altitude (m)");

    title( ...
        trajectory3DAxes, ...
        "Three-Dimensional UAV Flight Path and Bird Crossing");

    xlim( ...
        trajectory3DAxes, ...
        config.world.xLimits);

    ylim( ...
        trajectory3DAxes, ...
        config.world.yLimits);

    zlim( ...
        trajectory3DAxes, ...
        config.world.zLimits);

    view( ...
        trajectory3DAxes, ...
        38, ...
        25);

    axis( ...
        trajectory3DAxes, ...
        "vis3d");

    legend( ...
        trajectory3DAxes, ...
        "Location", ...
        "bestoutside");

    trajectory3DFile = fullfile( ...
        plotsDirectory, ...
        "02_mission_trajectory_3d.png");

    exportReportFigure( ...
        trajectory3DFigure, ...
        trajectory3DFile);

    reportOutputs.files(end + 1, 1) = trajectory3DFile;

    %% ---------------------------------------------------------------
    % 3. Flight speed and wind
    % ---------------------------------------------------------------
    speedWindFigure = createReportFigure( ...
        "Flight Speed and Wind", ...
        figureBackground);

    speedWindLayout = tiledlayout( ...
        speedWindFigure, ...
        2, ...
        1, ...
        "TileSpacing", ...
        "compact", ...
        "Padding", ...
        "compact");

    speedAxes = nexttile( ...
        speedWindLayout, ...
        1);

    hold(speedAxes, "on");

    configureAxes( ...
        speedAxes, ...
        axesBackground, ...
        foregroundColor, ...
        gridColor);

    addMissionPhaseBackgrounds( ...
        speedAxes, ...
        time, ...
        missionState);

    plot( ...
        speedAxes, ...
        time, ...
        groundSpeed, ...
        "LineWidth", ...
        1.8, ...
        "DisplayName", ...
        "Ground speed");

    plot( ...
        speedAxes, ...
        time, ...
        airSpeed, ...
        "LineWidth", ...
        1.6, ...
        "DisplayName", ...
        "Air-relative speed");

    plot( ...
        speedAxes, ...
        time, ...
        commandedSpeedEstimate, ...
        "--", ...
        "LineWidth", ...
        1.3, ...
        "DisplayName", ...
        "Estimated commanded speed");

    addWaypointMarkers( ...
        speedAxes, ...
        time, ...
        waypointIndex);

    xline( ...
        speedAxes, ...
        landingStartTime, ...
        ":", ...
        "Landing begins", ...
        "LabelVerticalAlignment", ...
        "middle");

    xline( ...
        speedAxes, ...
        touchdownTime, ...
        "--", ...
        "Touchdown", ...
        "LabelVerticalAlignment", ...
        "middle");

    ylabel( ...
        speedAxes, ...
        "Speed (m/s)");

    title( ...
        speedAxes, ...
        "UAV Speed Profile Across Mission Phases");

    legend( ...
        speedAxes, ...
        "Location", ...
        "bestoutside");

    windAxes = nexttile( ...
        speedWindLayout, ...
        2);

    hold(windAxes, "on");

    configureAxes( ...
        windAxes, ...
        axesBackground, ...
        foregroundColor, ...
        gridColor);

    addMissionPhaseBackgrounds( ...
        windAxes, ...
        time, ...
        missionState);

    plot( ...
        windAxes, ...
        time, ...
        windSpeed, ...
        "LineWidth", ...
        1.9, ...
        "DisplayName", ...
        "Wind magnitude");

    plot( ...
        windAxes, ...
        time, ...
        windX, ...
        "LineWidth", ...
        1.1, ...
        "DisplayName", ...
        "Wind X");

    plot( ...
        windAxes, ...
        time, ...
        windY, ...
        "LineWidth", ...
        1.1, ...
        "DisplayName", ...
        "Wind Y");

    plot( ...
        windAxes, ...
        time, ...
        windZ, ...
        "LineWidth", ...
        1.1, ...
        "DisplayName", ...
        "Wind Z");

    xlabel( ...
        windAxes, ...
        "Simulation time (s)");

    ylabel( ...
        windAxes, ...
        "Wind velocity (m/s)");

    title( ...
        windAxes, ...
        "Wind Disturbance Components");

    legend( ...
        windAxes, ...
        "Location", ...
        "bestoutside");

    title( ...
        speedWindLayout, ...
        "Mission Speed, Wind Disturbance, and Phase Transitions", ...
        "Color", ...
        foregroundColor, ...
        "FontWeight", ...
        "bold");

    speedWindFile = fullfile( ...
        plotsDirectory, ...
        "03_flight_speed_and_wind.png");

    exportReportFigure( ...
        speedWindFigure, ...
        speedWindFile);

    reportOutputs.files(end + 1, 1) = speedWindFile;

    %% ---------------------------------------------------------------
    % 4. Bird encounter and APF
    % ---------------------------------------------------------------
    birdFigure = createReportFigure( ...
        "Bird Encounter and APF", ...
        figureBackground);

    birdLayout = tiledlayout( ...
        birdFigure, ...
        3, ...
        1, ...
        "TileSpacing", ...
        "compact", ...
        "Padding", ...
        "compact");

    clearanceAxes = nexttile( ...
        birdLayout, ...
        1);

    hold(clearanceAxes, "on");

    configureAxes( ...
        clearanceAxes, ...
        axesBackground, ...
        foregroundColor, ...
        gridColor);

    plot( ...
        clearanceAxes, ...
        time, ...
        birdClearance, ...
        "LineWidth", ...
        2.1, ...
        "DisplayName", ...
        "Drone-to-bird surface clearance");

    yline( ...
        clearanceAxes, ...
        config.apf.safetyDistance, ...
        "--", ...
        "APF safety distance", ...
        "LabelVerticalAlignment", ...
        "bottom");

    yline( ...
        clearanceAxes, ...
        0, ...
        "-", ...
        "Collision boundary");

    if ~isempty(closestBirdIndex)

        scatter( ...
            clearanceAxes, ...
            time(closestBirdIndex), ...
            birdClearance(closestBirdIndex), ...
            95, ...
            "filled", ...
            "DisplayName", ...
            "Minimum clearance");

        text( ...
            clearanceAxes, ...
            time(closestBirdIndex), ...
            birdClearance(closestBirdIndex) + 0.18, ...
            sprintf( ...
                "%.2f m at %.2f s", ...
                birdClearance(closestBirdIndex), ...
                time(closestBirdIndex)), ...
            "Color", ...
            foregroundColor, ...
            "FontWeight", ...
            "bold", ...
            "HorizontalAlignment", ...
            "center");
    end

    ylabel( ...
        clearanceAxes, ...
        "Clearance (m)");

    title( ...
        clearanceAxes, ...
        "Dynamic Bird Encounter Clearance");

    legend( ...
        clearanceAxes, ...
        "Location", ...
        "bestoutside");

    apfAxes = nexttile( ...
        birdLayout, ...
        2);

    hold(apfAxes, "on");

    configureAxes( ...
        apfAxes, ...
        axesBackground, ...
        foregroundColor, ...
        gridColor);

    area( ...
        apfAxes, ...
        time, ...
        apfNumeric, ...
        "FaceAlpha", ...
        0.28, ...
        "EdgeColor", ...
        "none", ...
        "DisplayName", ...
        "APF active");

    stairs( ...
        apfAxes, ...
        time, ...
        emergencyNumeric, ...
        "LineWidth", ...
        1.6, ...
        "DisplayName", ...
        "Emergency avoidance");

    stairs( ...
        apfAxes, ...
        time, ...
        double(birdLidarHits > 0), ...
        "LineWidth", ...
        1.4, ...
        "DisplayName", ...
        "Bird detected by LiDAR");

    stairs( ...
        apfAxes, ...
        time, ...
        double(fpvBirdVisible), ...
        "LineWidth", ...
        1.2, ...
        "DisplayName", ...
        "Bird visible in FPV");

    ylim( ...
        apfAxes, ...
        [-0.05, 1.15]);

    ylabel( ...
        apfAxes, ...
        "Status");

    title( ...
        apfAxes, ...
        "Perception and Avoidance Activation Timeline");

    legend( ...
        apfAxes, ...
        "Location", ...
        "bestoutside");

    controllerAxes = nexttile( ...
        birdLayout, ...
        3);

    hold(controllerAxes, "on");

    configureAxes( ...
        controllerAxes, ...
        axesBackground, ...
        foregroundColor, ...
        gridColor);

    plot( ...
        controllerAxes, ...
        time, ...
        repulsiveSpeed, ...
        "LineWidth", ...
        1.8, ...
        "DisplayName", ...
        "Repulsive component");

    plot( ...
        controllerAxes, ...
        time, ...
        tangentialSpeed, ...
        "LineWidth", ...
        1.8, ...
        "DisplayName", ...
        "Tangential component");

    yyaxis( ...
        controllerAxes, ...
        "right");

    plot( ...
        controllerAxes, ...
        time, ...
        birdLidarHits, ...
        ":", ...
        "LineWidth", ...
        1.5, ...
        "DisplayName", ...
        "Bird LiDAR hits");

    ylabel( ...
        controllerAxes, ...
        "LiDAR hit count");

    yyaxis( ...
        controllerAxes, ...
        "left");

    xlabel( ...
        controllerAxes, ...
        "Simulation time (s)");

    ylabel( ...
        controllerAxes, ...
        "Avoidance velocity (m/s)");

    title( ...
        controllerAxes, ...
        "APF Controller Response During Bird Crossing");

    legend( ...
        controllerAxes, ...
        "Location", ...
        "bestoutside");

    title( ...
        birdLayout, ...
        "Dynamic Bird Detection, Clearance, and APF Response", ...
        "Color", ...
        foregroundColor, ...
        "FontWeight", ...
        "bold");

    birdFile = fullfile( ...
        plotsDirectory, ...
        "04_bird_encounter_and_apf.png");

    exportReportFigure( ...
        birdFigure, ...
        birdFile);

    reportOutputs.files(end + 1, 1) = birdFile;

    %% ---------------------------------------------------------------
    % 5. Final occupancy map
    % ---------------------------------------------------------------
    occupancyFigure = createReportFigure( ...
        "Final Occupancy Map", ...
        figureBackground);

    occupancyAxes = axes( ...
        "Parent", ...
        occupancyFigure);

    configureAxes( ...
        occupancyAxes, ...
        axesBackground, ...
        foregroundColor, ...
        gridColor);


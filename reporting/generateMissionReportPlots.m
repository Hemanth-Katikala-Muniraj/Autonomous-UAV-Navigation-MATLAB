function plotActiveTrajectorySegments( ...
    axesHandle, positionX, positionY, activeMask, displayName)
%PLOTACTIVETRAJECTORYSEGMENTS Highlight active-controller path sections.

    activeIndices = find( ...
        activeMask);

    if isempty(activeIndices)
        return;
    end

    segmentStart = ...
        activeIndices(1);

    previousIndex = ...
        activeIndices(1);

    legendUsed = false;

    for activeIndex = 2:numel(activeIndices)

        currentIndex = ...
            activeIndices(activeIndex);

        if currentIndex > previousIndex + 1

            legendUsed = plotOneActiveSegment( ...
                axesHandle, ...
                positionX, ...
                positionY, ...
                segmentStart, ...
                previousIndex, ...
                displayName, ...
                legendUsed);

            segmentStart = ...
                currentIndex;
        end

        previousIndex = ...
            currentIndex;
    end

    plotOneActiveSegment( ...
        axesHandle, ...
        positionX, ...
        positionY, ...
        segmentStart, ...
        previousIndex, ...
        displayName, ...
        legendUsed);
end


function legendUsed = plotOneActiveSegment( ...
    axesHandle, positionX, positionY, ...
    startIndex, endIndex, displayName, legendUsed)
%PLOTONEACTIVESEGMENT Draw one contiguous APF trajectory segment.

    segmentIndices = ...
        max(1, startIndex - 1): ...
        min(numel(positionX), endIndex + 1);

    if legendUsed

        plot( ...
            axesHandle, ...
            positionX(segmentIndices), ...
            positionY(segmentIndices), ...
            "-", ...
            "LineWidth", ...
            4.0, ...
            "HandleVisibility", ...
            "off");

    else

        plot( ...
            axesHandle, ...
            positionX(segmentIndices), ...
            positionY(segmentIndices), ...
            "-", ...
            "LineWidth", ...
            4.0, ...
            "DisplayName", ...
            displayName);

        legendUsed = true;
    end
end


function addDirectionArrows2D(axesHandle, xData, yData)
%ADDDIRECTIONARROWS2D Add sparse direction arrows to the path.

    validMask = ...
        isfinite(xData) & ...
        isfinite(yData);

    validIndices = ...
        find(validMask);

    if numel(validIndices) < 4
        return;
    end

    arrowIndices = round( ...
        linspace( ...
            validIndices(1), ...
            validIndices(end) - 1, ...
            7));

    for arrowIndex = arrowIndices

        nextIndex = ...
            min( ...
                arrowIndex + 2, ...
                numel(xData));

        deltaX = ...
            xData(nextIndex) - ...
            xData(arrowIndex);

        deltaY = ...
            yData(nextIndex) - ...
            yData(arrowIndex);

        if hypot(deltaX, deltaY) < 1e-4
            continue;
        end

        quiver( ...
            axesHandle, ...
            xData(arrowIndex), ...
            yData(arrowIndex), ...
            deltaX, ...
            deltaY, ...
            0, ...
            "LineWidth", ...
            1.1, ...
            "MaxHeadSize", ...
            1.8, ...
            "HandleVisibility", ...
            "off");
    end
end


function addMissionPhaseBackgrounds( ...
    axesHandle, time, missionState)
%ADDMISSIONPHASEBACKGROUNDS Add subtle mission-state regions.

    stateNames = {
        "TAKEOFF"
        "NAVIGATION"
        "LANDING_APPROACH"
        "LANDING_DESCENT"
        "LANDING_FLARE"
        "TOUCHDOWN_VERIFY"
        "COMPLETE"
    };

    axesLimits = ...
        ylim(axesHandle);

    for stateIndex = 1:numel(stateNames)

        currentState = ...
            stateNames{stateIndex};

        stateMask = ...
            missionState == currentState;

        stateRanges = ...
            logicalRanges( ...
                stateMask);

        for rangeIndex = 1:size(stateRanges, 1)

            startIndex = ...
                stateRanges(rangeIndex, 1);

            endIndex = ...
                stateRanges(rangeIndex, 2);

            patch( ...
                axesHandle, ...
                [ ...
                    time(startIndex), ...
                    time(endIndex), ...
                    time(endIndex), ...
                    time(startIndex)], ...
                [ ...
                    axesLimits(1), ...
                    axesLimits(1), ...
                    axesLimits(2), ...
                    axesLimits(2)], ...
                0.5 * ones(1, 3), ...
                "FaceAlpha", ...
                0.055, ...
                "EdgeColor", ...
                "none", ...
                "HandleVisibility", ...
                "off");
        end
    end

    uistack( ...
        findobj( ...
            axesHandle, ...
            "Type", ...
            "patch"), ...
        "bottom");
end



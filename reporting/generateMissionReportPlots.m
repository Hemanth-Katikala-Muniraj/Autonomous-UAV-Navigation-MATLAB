function summaryText = getSummaryText( ...
    summaryData, fieldName, defaultText)
%GETSUMMARYTEXT Retrieve a summary value as display text.

    summaryText = ...
        string(defaultText);

    if istable(summaryData) && ...
            ismember( ...
                fieldName, ...
                summaryData.Properties.VariableNames)

        summaryText = ...
            string( ...
                summaryData.(fieldName)(1));

    elseif isstruct(summaryData) && ...
            isfield(summaryData, fieldName)

        summaryText = ...
            string( ...
                summaryData.(fieldName));
    end

    if summaryText == "1" || ...
            lower(summaryText) == "true"

        summaryText = ...
            "Successful";
    end
end


function metricText = formatSummaryMetric( ...
    summaryData, fieldName, fallbackValue, formatText)
%FORMATSUMMARYMETRIC Retrieve and format one mission metric.

    metricValue = ...
        fallbackValue;

    if istable(summaryData) && ...
            ismember( ...
                fieldName, ...
                summaryData.Properties.VariableNames)

        candidateValue = ...
            summaryData.(fieldName)(1);

        if isnumeric(candidateValue)

            metricValue = ...
                double(candidateValue);
        end

    elseif isstruct(summaryData) && ...
            isfield(summaryData, fieldName)

        candidateValue = ...
            summaryData.(fieldName);

        if isnumeric(candidateValue)

            metricValue = ...
                double(candidateValue);
        end
    end

    if isempty(metricValue) || ...
            ~isfinite(metricValue)

        metricText = ...
            "Not recorded";

    else

        metricText = ...
            string( ...
                sprintf( ...
                    formatText, ...
                    metricValue));
    end
end
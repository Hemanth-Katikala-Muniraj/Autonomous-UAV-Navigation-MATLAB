function telemetryTable = loadTelemetryTable(telemetryInput)
%LOADTELEMETRYTABLE Load telemetry from a table or CSV path.

    if istable(telemetryInput)

        telemetryTable = ...
            telemetryInput;

        return;
    end

    telemetryPath = ...
        string(telemetryInput);

    if ~isfile(telemetryPath)

        error( ...
            "MissionReport:TelemetryFileMissing", ...
            "Telemetry file was not found: %s", ...
            telemetryPath);
    end

    importOptions = ...
        detectImportOptions( ...
            telemetryPath, ...
            "VariableNamingRule", ...
            "preserve");

    telemetryTable = ...
        readtable( ...
            telemetryPath, ...
            importOptions);
end



                expectedRows, ...
                1);
    end
end


function outputColumn = getLogicalColumn( ...
    inputTable, variableName, expectedRows)
%GETLOGICALCOLUMN Read a logical telemetry column safely.

    if ismember( ...
            variableName, ...
            inputTable.Properties.VariableNames)

        rawValue = ...
            inputTable.(variableName);

        if islogical(rawValue)

            outputColumn = ...
                rawValue(:);

        elseif isnumeric(rawValue)

            outputColumn = ...
                rawValue(:) ~= 0;

        else

            outputColumn = ...
                strcmpi( ...
                    string(rawValue(:)), ...
                    "true");
        end

    else

        outputColumn = ...
            false( ...
                expectedRows, ...
                1);
    end
end



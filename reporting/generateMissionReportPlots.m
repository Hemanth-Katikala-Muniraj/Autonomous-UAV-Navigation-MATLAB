function commandedSpeed = estimateCommandedSpeed( ...
    time, positionX, positionY, positionZ)
%ESTIMATECOMMANDEDSPEED Estimate path command speed from position changes.

    deltaTime = [
        NaN
        diff(time)
    ];

    distanceStep = [
        0
        sqrt( ...
            diff(positionX).^2 + ...
            diff(positionY).^2 + ...
            diff(positionZ).^2)
    ];

    commandedSpeed = ...
        distanceStep ./ ...
        max(deltaTime, eps);

    commandedSpeed(1) = ...
        commandedSpeed( ...
            min(2, numel(commandedSpeed)));

    commandedSpeed = ...
        movmean( ...
            commandedSpeed, ...
            5, ...
            "omitnan");
end


function minimumIndex = findFiniteMinimumIndex(inputData)
%FINDFINITEMINIMUMINDEX Find the index of the minimum finite value.

    validIndices = ...
        find(isfinite(inputData));

    if isempty(validIndices)

        minimumIndex = [];

        return;
    end

    [~, localIndex] = min( ...
        inputData(validIndices));

    minimumIndex = ...
        validIndices(localIndex);
end


function outputColumn = getNumericColumn( ...

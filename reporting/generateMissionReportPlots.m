function ranges = logicalRanges(logicalMask)
%LOGICALRANGES Return start and end indices of true regions.

    logicalMask = ...
        logical(logicalMask(:));

    paddedMask = [
        false
        logicalMask
        false
    ];

    transitions = ...
        diff(paddedMask);

    startIndices = ...
        find(transitions == 1);

    endIndices = ...
        find(transitions == -1) - 1;

    ranges = [
        startIndices, ...
        endIndices
    ];
end



function occupancyProbability = getOccupancyProbability(occupancyGrid)
%GETOCCUPANCYPROBABILITY Extract occupancy probabilities robustly.

    occupancyProbability = [];

    candidateFields = {
        "probabilityGrid"
        "occupancyProbability"
        "probability"
        "map"
        "grid"
    };

    for candidateIndex = 1:numel(candidateFields)

        fieldName = ...
            candidateFields{candidateIndex};

        if isfield(occupancyGrid, fieldName)

            candidateValue = ...
                occupancyGrid.(fieldName);

            if isnumeric(candidateValue) && ...
                    ismatrix(candidateValue)

                occupancyProbability = ...
                    double(candidateValue);

                break;
            end
        end
    end

    if isempty(occupancyProbability) && ...
            isfield(occupancyGrid, "logOdds")

        logOdds = ...
            double(occupancyGrid.logOdds);

        occupancyProbability = ...
            1 ./ ...
            (1 + exp(-logOdds));
    end

    if isempty(occupancyProbability)

        occupancyProbability = ...
            zeros( ...
                48, ...
                120);
    end

    if max( ...
            occupancyProbability, ...
            [], ...
            "all", ...
            "omitnan") > 1

        occupancyProbability = ...
            1 ./ ...
            (1 + exp(-occupancyProbability));
    end
end


